	<p id="t01"><h1>Residential Area</h1>
	1. Hire qualified / experienced chowkidars only.<br>
	2.Check the antecedents / credentials of chowkidars and domestic servants before hiring them. Insist on references from their previous employers. Furnish their full particulars to your police station in the format suggested by them.<br>
	3. Always employ different chowkidars for day and night duties.<br>
	4. Secretaries of Societies are advised to brief all the security men / chowkidars / lift men about various aspects of security. Some of these are:
    <ul>
    <li>To make an enquiry if a taxi or auto-rickshaw is seen parked inside the compound or outside the building for a long time.</li>
    <li>To inform the police in case they find any vehicle parked inside the building (if ownership is not known) for a long time.</li></ul>
	5. Secretaries of societies are advised to brief all residents on the following: 
    <ul>
   	<li>Whenever they employ a new servant, the local police station should be informed. His or her photographs, fingerprints and other details should be obtained and submitted to local police station.</li>
    <li>They should be advised to install a door with iron bars inside the flat, superior quality night latches and special eye lens on the main door.Ideally, interaction with strangers and vendors should be through the grilled door. High wattage light source with pilfer proof cover outside the main door should be installed.</li>
    <li>They should not keep huge amounts of cash, valuable ornaments etc., in the house.</li>
    <li>They should avoid discussing money transactions and other important family matters in the presence of servants/ outsiders.</li>
    <li>They should not humiliate servants on petty matters. Nor should they penalize them for small damages.</li>
	<li>It is important to inform the police about any suspicious happening or unclaimed object found in suspicious circumstances. Extend full cooperation to the police.</li></ul></p>
	<p id="t02"><h1>Vehicle theft</h1>
	<strong>WHAT YOU SHOULD DO...</strong><br>
	Use securing devices like steering lock, clutch lock, brake lock, etc.<br>
    Double-check all doors, including the boot.<br>
    If possible, install loud alarm systems in your car, so that thieves can be discouraged even if they manage to break into your car.<br>
    Try and use detachable music systems and take them with you whenever you park the car for a long time, so that there is no temptation for thieves to make a quick steal when they see expensive items in parked cars.<br>
    Paint your car number on the front and rear end of the body, apart from the number plates. Ideally, have it etched on the windscreens and window glasses too. This prevents them from being misused by criminals using fake number plates.<br>
	<strong>WHAT YOU SHOULD NOT DO...</strong><br>
	Never leave the vehicle door unlocked, nor the windows partially open. Make sure that the quarter glasses are properly secured.<br>
    Do not leave valuables inside the car, even if it is locked, as this will attract thieves.<br>
    Do not overdo extra fittings, as these tempt prospective thieves.<br>
	Never leave the key dangling in the ignition.<br></p>
	<p id="t03"><h1>Fraud money schemes</h1>
		<strong>WHAT THEY SAY</strong>
		<ul>
		<li>Double your money in two months or less.</li>
		<li>Post dated cheques as a guarantee for the same.</li>
		<li>Claim they do it by investing in some wonder projects or plantations.</li>
		<li>Huge sums of money by being a part of the gift chain i.e. you gift a certain amount of money to two people, then find two people who will enter the chain by gifting you the sum. They in turn will get two more subscribers, and so on.</li>
		<li>As the scheme is run by a registered company, it is absolutely safe.</li></ul>
		<strong>REALITY</strong>
		<ul><li>All schemes offering high returns of double money within two months or such short spans have no legal sanction, nor they are reliable.</li>
		<li>Postdated cheques is not a guaranteed payment device, as they could bounce when you finally present them.</li>
		<li>There are no projects, especially agriculture based, which can give returns in less than a year. In fact, tree plantations take at least 10 years to mature.</li>
		<li>Gift schemes and chains are an offence under the Prize Chits and Money Circulation Schemes (Banning) Act of 1978.</li>
		<li>Even registered companies are not allowed to promote and run any such schemes.</li></ul></p>
